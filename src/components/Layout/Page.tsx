import { MouseEventHandler } from "react";

import Button from "../Elements/Button";
import PageList from "./PageList";
import OptionsItem from "../Options/OptionsItem";
import OptionsButton from "../Options/OptionsButton";
import OptionsList from "../Options/OptionsList";

import useNavStore from "@/stores/useModalStore";
import useAuthStore from "@/features/auth/stores/useAuthStore";
import useOptions from "@/hooks/useOptions";

import { useShareLink } from "@/features/recipes/api/permissions/createShareLink";

interface IProps {
  back: () => void;
  color: string;
  isChanged: boolean;
  ownerId: number;
  cardId: number;
  path: "recipes" | "grocery-lists";
  toggleEditMode: () => void;
  children?: React.ReactNode;
  updateFn: MouseEventHandler<HTMLButtonElement>;
}

const Page = ({
  back,
  color,
  isChanged,
  ownerId,
  cardId,
  path,
  toggleEditMode,
  children,
  updateFn,
}: IProps) => {
  const { toggleOpen } = useNavStore();
  const { user } = useAuthStore();
  const { optionsOpen, toggleOptions, listRef, handleBlur } = useOptions();

  const shareLink = useShareLink();

  return (
    <div className={`fixed inset-0 z-10 h-full w-full ${color}`}>
      <header className="fixed top-0 z-10 w-full border-b-2 border-black bg-inherit">
        <div className="mx-auto flex h-16 w-full  max-w-2xl items-center  justify-between bg-inherit p-4">
          <Button onClick={back} aria-label="back">
            <svg
              xmlns="http://www.w3.org/2000/svg"
              fill="none"
              viewBox="0 0 24 24"
              strokeWidth={1.5}
              stroke="currentColor"
              className="h-6 w-6"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                d="M9 15L3 9m0 0l6-6M3 9h12a6 6 0 010 12h-3"
              />
            </svg>
          </Button>
          <div className="flex gap-4">
            {shareLink.data ? (
              <Button onClick={shareLink.copy} aria-label="copy link">
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  fill="none"
                  viewBox="0 0 24 24"
                  strokeWidth={1.5}
                  stroke="currentColor"
                  className="h-6 w-6"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    d="M15.75 17.25v3.375c0 .621-.504 1.125-1.125 1.125h-9.75a1.125 1.125 0 01-1.125-1.125V7.875c0-.621.504-1.125 1.125-1.125H6.75a9.06 9.06 0 011.5.124m7.5 10.376h3.375c.621 0 1.125-.504 1.125-1.125V11.25c0-4.46-3.243-8.161-7.5-8.876a9.06 9.06 0 00-1.5-.124H9.375c-.621 0-1.125.504-1.125 1.125v3.5m7.5 10.375H9.375a1.125 1.125 0 01-1.125-1.125v-9.25m12 6.625v-1.875a3.375 3.375 0 00-3.375-3.375h-1.5a1.125 1.125 0 01-1.125-1.125v-1.5a3.375 3.375 0 00-3.375-3.375H9.75"
                  />
                </svg>
              </Button>
            ) : (
              <Button
                onClick={() =>
                  shareLink.mutateAsync({ id: cardId, path: path })
                }
                aria-label="get share link"
              >
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  fill="none"
                  viewBox="0 0 24 24"
                  strokeWidth={1.5}
                  stroke="currentColor"
                  className="h-6 w-6"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    d="M7.217 10.907a2.25 2.25 0 100 2.186m0-2.186c.18.324.283.696.283 1.093s-.103.77-.283 1.093m0-2.186l9.566-5.314m-9.566 7.5l9.566 5.314m0 0a2.25 2.25 0 103.935 2.186 2.25 2.25 0 00-3.935-2.186zm0-12.814a2.25 2.25 0 103.933-2.185 2.25 2.25 0 00-3.933 2.185z"
                  />
                </svg>
              </Button>
            )}
            {isChanged ? (
              <Button onClick={updateFn} aria-label="save changes">
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  height="28px"
                  viewBox="0 0 24 24"
                  width="28px"
                  fill="black"
                >
                  <path d="M0 0h24v24H0V0z" fill="none" />
                  <path d="M17 3H5c-1.11 0-2 .9-2 2v14c0 1.1.89 2 2 2h14c1.1 0 2-.9 2-2V7l-4-4zm2 16H5V5h11.17L19 7.83V19zm-7-7c-1.66 0-3 1.34-3 3s1.34 3 3 3 3-1.34 3-3-1.34-3-3-3zM6 6h9v4H6z" />
                </svg>
              </Button>
            ) : (
              <Button onClick={toggleEditMode} aria-label="edit mode">
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  fill="none"
                  viewBox="0 0 24 24"
                  strokeWidth={1.5}
                  stroke="currentColor"
                  className="h-6 w-6"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    d="M16.862 4.487l1.687-1.688a1.875 1.875 0 112.652 2.652L10.582 16.07a4.5 4.5 0 01-1.897 1.13L6 18l.8-2.685a4.5 4.5 0 011.13-1.897l8.932-8.931zm0 0L19.5 7.125M18 14v4.75A2.25 2.25 0 0115.75 21H5.25A2.25 2.25 0 013 18.75V8.25A2.25 2.25 0 015.25 6H10"
                  />
                </svg>
              </Button>
            )}
            <OptionsButton
              isOpen={optionsOpen}
              toggleOpen={toggleOptions}
              screen="page"
            />
          </div>
          {optionsOpen && (
            <OptionsList
              closeOptions={toggleOptions}
              listRef={listRef}
              handleBlur={handleBlur}
            >
              <OptionsItem
                onClick={() => toggleOpen("permissions")}
                isHidden={user?.id !== ownerId}
                key="permissions"
              >
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  fill="none"
                  viewBox="0 0 24 24"
                  strokeWidth={1.5}
                  stroke="currentColor"
                  className="h-6 w-6"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    d="M13.5 10.5V6.75a4.5 4.5 0 119 0v3.75M3.75 21.75h10.5a2.25 2.25 0 002.25-2.25v-6.75a2.25 2.25 0 00-2.25-2.25H3.75a2.25 2.25 0 00-2.25 2.25v6.75a2.25 2.25 0 002.25 2.25z"
                  />
                </svg>
                Permissions
              </OptionsItem>

              <OptionsItem
                onClick={() => toggleOpen("leave")}
                key="leave"
                isLast
                isHidden={user?.id === ownerId}
              >
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  fill="none"
                  viewBox="0 0 24 24"
                  strokeWidth={1.5}
                  stroke="currentColor"
                  className="h-6 w-6"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    d="M22 10.5h-6m-2.25-4.125a3.375 3.375 0 11-6.75 0 3.375 3.375 0 016.75 0zM4 19.235v-.11a6.375 6.375 0 0112.75 0v.109A12.318 12.318 0 0110.374 21c-2.331 0-4.512-.645-6.374-1.766z"
                  />
                </svg>
                Leave
              </OptionsItem>
              <OptionsItem
                onClick={() => toggleOpen("delete")}
                key="delete"
                isHidden={user?.id !== ownerId}
                isLast
              >
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  fill="none"
                  viewBox="0 0 24 24"
                  strokeWidth={1.5}
                  stroke="currentColor"
                  className="h-6 w-6"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    d="M14.74 9l-.346 9m-4.788 0L9.26 9m9.968-3.21c.342.052.682.107 1.022.166m-1.022-.165L18.16 19.673a2.25 2.25 0 01-2.244 2.077H8.084a2.25 2.25 0 01-2.244-2.077L4.772 5.79m14.456 0a48.108 48.108 0 00-3.478-.397m-12 .562c.34-.059.68-.114 1.022-.165m0 0a48.11 48.11 0 013.478-.397m7.5 0v-.916c0-1.18-.91-2.164-2.09-2.201a51.964 51.964 0 00-3.32 0c-1.18.037-2.09 1.022-2.09 2.201v.916m7.5 0a48.667 48.667 0 00-7.5 0"
                  />
                </svg>
                Delete
              </OptionsItem>
            </OptionsList>
          )}
        </div>
      </header>
      <PageList>{children}</PageList>
    </div>
  );
};

export default Page;
